/**
 * Склонение русских существительных по числу.
 * pluralize(1, "концерт", "концерта", "концертов") → "концерт"
 * pluralize(2, "концерт", "концерта", "концертов") → "концерта"
 * pluralize(5, "концерт", "концерта", "концертов") → "концертов"
 * pluralize(21, "концерт", "концерта", "концертов") → "концерт"
 * pluralize(42, "концерт", "концерта", "концертов") → "концерта"
 *
 * Правило:
 * - 1, 21, 31, 41... → "концерт" (единственное число)
 * - 2-4, 22-24, 32-34... → "концерта" (родительный единств.)
 * - 5-20, 25-30, 35-40... → "концертов" (родительный множ.)
 */
export function pluralize(n: number, one: string, few: string, many: string): string {
  const abs = Math.abs(n);
  const mod10 = abs % 10;
  const mod100 = abs % 100;

  if (mod100 >= 11 && mod100 <= 19) return many;
  if (mod10 === 1) return one;
  if (mod10 >= 2 && mod10 <= 4) return few;
  return many;
}
